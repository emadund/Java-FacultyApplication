    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        lblNameError.setText("");
        lblEspbError.setText("");
        try {
            String name = txtName.getText().trim();
            if (name.isEmpty()) {
                lblNameError.setText("Morate uneti ime predmeta!");
                return;
            }

            try {
                int espb = Integer.parseInt(txtEspb.getText().trim());
                Department department = (Department) comboDepartment.getSelectedItem();

                Subject subject = new Subject(name, espb, department);
                subjectRepository.save(subject);
                fillSubjectTable();
                //osvezi tabelu....

            } catch (NumberFormatException nfe) {
                lblEspbError.setText("Niste uneli broj!");
            } catch (Exception ex) {
                lblMessage.setText(ex.getMessage());
            }

        } catch (Exception e) {
            System.out.println("Desila se greska: " + e.getMessage());
        }


    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        try {
            String name = txtNameUpdate.getText();
            int espb = Integer.parseInt(txtEspbUpdate.getText().trim());
            Department department = (Department) comboDepartmentUpdate.getSelectedItem();
            
            Subject subject = new Subject(name, espb, department);
            subjectRepository.update(subject);
            
            fillSubjectTable();

        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void tblSubjectsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSubjectsMouseClicked
        pnlUpdateSubject.setVisible(true);
        int selectedRow = tblSubjects.getSelectedRow();

        if (selectedRow != -1) {
            String name = tblSubjects.getValueAt(selectedRow, 0).toString();
            int espb = Integer.parseInt(tblSubjects.getValueAt(selectedRow, 1).toString());
            Department department = (Department) tblSubjects.getValueAt(selectedRow, 2);

            txtNameUpdate.setText(name);
            txtEspbUpdate.setText(espb + "");
            comboDepartmentUpdate.setSelectedItem(department);
        }

    }//GEN-LAST:event_tblSubjectsMouseClicked

