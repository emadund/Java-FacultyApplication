    private void fillComboDepartment() {
        //popuni kombobox sa podacima
        List<Department> departments = departmentRepository.getAll();
        for (Department department : departments) {
            // comboDepartment.addItem(department.getName() + "(" + department.getShortname() +")");
            comboDepartment.addItem(department);
            comboDepartmentUpdate.addItem(department);
        }
    }

    private void fillSubjectTable() {
        //clearTableSubjects();
        List<Subject> subjects = subjectRepository.getAll();
        DefaultTableModel dtm = (DefaultTableModel) tblSubjects.getModel();
        dtm.setRowCount(0);

        for (Subject s : subjects) {
            Object[] rowData = new Object[3];
            rowData[0] = s.getName();
            rowData[1] = s.getEspb();
            rowData[2] = s.getDepartment();
            dtm.addRow(rowData);
        }

    }

    private void clearTableSubjects() {
        tblSubjects.setModel(new javax.swing.table.DefaultTableModel(
                new Object[][]{},
                new String[]{
                    "Name", "ESPB", "Department"
                }
        ) {
            boolean[] canEdit = new boolean[]{
                false, false, false
            };

            public boolean isCellEditable(int rowIndex, int columnIndex) {
                return canEdit[columnIndex];
            }
        });

    }
